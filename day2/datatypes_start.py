## Checking the varaible types

# Basic Datatypes
a = 10
print(a,type(a))

a = 5.5
print(a, type(a))

a = 'Teja'
print(a, type(a))

a = True
print(a, type(a))

a = None
print(a, type(a))

# Sequence Types
a = [23,25,36]
print(a, type(a))

a = (23,25,36)
print(a, type(a))

a = {23,45,36}
print(a, type(a))

a = {1:"Ramesh",2:"Suresh"}
print(a, type(a))

