## Checking the varaible types

# Basic Datatypes
a = 10
print(a,type(a))

a = 2.3
print(a,type(a))

a = 'Teja'
print(a,type(a))

a = True
print(a,type(a))

a = None
print(a,type(a))

# Sequence Types
a = [1,2,3]
print(a,type(a))

a = (1,2,3)
print(a,type(a))

a = {23,2,43}
print(a, type(a))

a = {1:'Ravi',2:'Rajesh'}
print(a, type(a))

